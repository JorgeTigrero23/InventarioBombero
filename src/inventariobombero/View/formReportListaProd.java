    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try {
            BDConexion con = new BDConexion();
            Connection cn = con.getConnection();
                
            //archivo jasper
            URL jasperUrl = this.getClass().getResource("/inventariobombero/Report/rep_ListadoEquipo.jasper");
            JasperReport report = (JasperReport) JRLoader.loadObject(jasperUrl);
            JasperPrint jasperPrint = JasperFillManager.fillReport(report, null, cn);
            //se crea el visor con el reporte
            JRViewer jRViewer = new JRViewer(jasperPrint);
            //se elimina elementos del contenedor JPanel
            jp.removeAll();
            //para el tama√±o de l reporte se agrega un BorderLayout
            jp.setLayout(new BorderLayout());      
            jp.add(jRViewer);
            jRViewer.setVisible(true);
            
            jp.repaint();
            jp.revalidate();
         } catch (JRException ex) {
            System.err.println(ex.getMessage());
         }
    }//GEN-LAST:event_jButton1ActionPerformed


